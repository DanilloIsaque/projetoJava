public void listarVendas(){

    try {
            //primeiro chamamos a dao de categoria
            vendasDAO objvendasdao = new vendasDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaVendas.getModel();
            model.setNumRows(0);

            ArrayList<vendasDTO> listaVendas = objvendasdao.listarVendas();

            for (int i = 0; i < listaVendas.size(); i++) {
                model.addRow(new Object[]{//adiciona linha e abro o objeto para montar as colunas
                    listaVendas.get(i).getId_produto(),
                    listaVendas.get(i).getId_venda(),
                    listaVendas.get(i).getQtd_produto(),
                    listaVendas.get(i).getData_venda(),});

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro no listar vendas frmVendasView" + erro);
        }
}



